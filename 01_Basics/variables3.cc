#include <iostream>

int main()
{
    bool my_var0 = true; // 1 Byte

    signed char my_val1 = 10; // 1 Byte
    short my_val2 = 300;      // 2 Byte
    int my_val3 = 5000;       // 4 Byte
    long my_val4 = 100000;    // 8 Byte

    float my_val5 = 10.0f;  // 4 Byte
    double my_val6 = 100.0; // 8 Byte

    return 0;
}
